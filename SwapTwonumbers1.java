package javaProjectsPrograms;

public class SwapTwonumbers1 {

	public static void main(String[] args) {
		int a=30;
		int b=20;
		System.out.println("Before swapping");
		System.out.println(a);
		System.out.println(b);
		b=b+a;
		a=b-a;
		b=b-a;
		System.out.println("After swapping");
		System.out.println(a);
		System.out.println(b);
		
		
		
		

	}

}
